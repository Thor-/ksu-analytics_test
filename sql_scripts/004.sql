INSERT INTO [dbo].[Faculty$]
           ([Faculty])
select [FacultyName]
from [dbo].[Departments_tmp] d
where not exists(select 1 from [dbo].[Faculty$] f where f.Faculty = d.[FacultyName])
GO

INSERT INTO [dbo].[Specialties$]
           ([Specialty])
select [SpecialtyName]
from [dbo].[Departments_tmp] d
where not exists(select 1 from [dbo].[Specialties$] s where s.Specialty = d.[SpecialtyName])
GO



update [dbo].[Specialty_sources$]
  set [Department] = d.id
from [dbo].[Specialty_sources$] ss
	join [dbo].[Specialties$] s on ss.Specialty = s.id
	join [dbo].[Departments_tmp] dt on s.Specialty = dt.[SpecialtyName]
	join [dbo].[Departments$] d on d.[Faculty] = ss.[Faculty] and dt.[DepartmentName] = d.[Department]
GO


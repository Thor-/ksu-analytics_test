	Id int identity(1,1) primary key,
	[Faculty] int NOT NULL,
	[Department] [nvarchar](100) NOT NULL,
    CONSTRAINT FK_Departments$_Faculty$ FOREIGN KEY (Faculty) REFERENCES dbo.Faculty$ (id) ON UPDATE  NO ACTION ON DELETE  NO ACTION 
) ON [PRIMARY]
GO


INSERT INTO [dbo].[Departments$]
           ([Faculty]
           ,[Department])
select 
(select top(1) f.id from [dbo].[Faculty$] f where d.[FacultyName] = f.Faculty), 
 d.[DepartmentName]
from Departments_tmp d
group by d.[DepartmentName], d.[FacultyName]
GO

